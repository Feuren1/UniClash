
class Critter {
    baseHealth: number;
    baseAttack: number;
    baseDefence: number;
    baseSpeed: number;

    constructor(baseHealth: number, baseAttack: number, baseDefence: number, baseSpeed: number) {
        this.baseHealth = baseHealth;
        this.baseAttack = baseAttack;
        this.baseDefence = baseDefence;
        this.baseSpeed = baseSpeed;
    }
}
